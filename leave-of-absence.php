                            <p>Students sometimes find themselves facing difficulties that affect their ability to undertake their studies, for example, if they are ill or undergoing serious personal problems. A leave of absence enables a student to take an authorised break from their studies in order to overcome the difficulty, without losing valuable time from their registration period, which will be suspended for the duration.</p>
                            <p>Students must apply for permission from their department and faculty to take a leave of absence, by sending an email officially to <a href="mailto:academics@msbm.org.uk">academics@msbm.org.uk</a>. Please be aware that once the email is received by the Academics Department, it will normally take up to 2 weeks for Faculty consideration and processing, but may take longer if additional information/clarification is required. Students can apply for LOA on the following grounds:</p>
                            <ul>
                                <li>Medical</li>
                                <li>Personal</li>
                                <li>Parental (maternity/paternity)</li>
                                <li>Professional/Work related</li>
                                <li>Financial</li>
                            </ul>
                            <p>Requests for LOA on financial grounds are not normally approved but may be considered if a student’s financial situation changes due to unforeseen and exceptional circumstances.</p>
                            <p>Students should not apply for a leave of absence to cover a period of annual leave.</p>
                            <p>When applying for a leave of absence, appropriate documentary evidence must be supplied in order for the department and faculty to consider whether to approve the request. For a leave of absence on medical grounds, a medical or doctor’s note is required that covers the entire period of leave of absence requested. Any application made on medical grounds without an appropriate medical note will not be approved.</p>
                            <p>Students applying for a leave of absence to cover maternity leave should, wherever possible, provide a copy of their ‘MAT’ form.</p>
                            <p>It is essential that students notify their supervisor/department as soon as any difficulties arise that might affect their studies and that applications for leave of absence are made promptly and, wherever possible, not retrospectively. Requests for retrospective leave of absence will not normally be approved if they date back further than 45 days.</p>
                            <p>Absences that are taken without a formal request for leave of absence being made will be treated as unauthorised absences and may trigger further action. During the period of leave of absence, the student will temporarily leave their programme of study and their registration will be suspended for the duration of the absence. Supervision should not be provided during a leave of absence and the student should not undertake any work that relates to their studies.</p>
                            <p>Students should be aware that requests for indefinite leave of absence will not be considered. It is unlikely that a request for more than 12 months at a time will be approved and requests to further extend a lengthy leave of absence will be carefully reviewed and may be rejected. There are a number of reasons why lengthy periods away from your studies are not recommended, e.g. the possibility that the study will lose currency and/or originality and changes to departmental structures and staffing, which may mean that appropriate supervision is no longer available.</p>
                            <p>In certain specific circumstances, there may be occasions when a student is returning from a leave of absence due to ill health or maternity leave but is not yet able to undertake their study full time. Under such circumstances, a ‘phased return’ to work may be considered. In practice, a student would need to apply for a leave of absence, which, if granted, would cover only a proportional amount of time lost due to these exceptional circumstances, and not the entire period during which these circumstances are a factor.</p>
                            <p>Students who receive financial support to undertake their study with us, i.e. those funded by Research Councils or by an employer or an overseas government, should notify their sponsor when applying for a leave of absence, in case there are any restrictions.</p>
